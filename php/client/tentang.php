                <p>
                    Sistem Management Lab adalah aplikasi berbasis web yang dirancang untuk membantu pengelolaan laboratorium secara digital. 
                    Aplikasi ini memungkinkan admin atau petugas lab untuk mengelola data peminjaman barang, pengguna lab, serta kategori barang secara terstruktur dan efisien.
                </p>
                <h2>Fitur Utama:</h2>
                <ul>
                    <li>Manajemen Data Peminjaman</li>
                    <li>Pengelolaan Data Pengguna Lab (kelas, mahasiswa, dosen)</li>
                    <li>Pengelompokan dan Kategori Barang</li>
                    <li>Dashboard Informasi Ringkas</li>
                    <li>Login Multi-Level (Admin, Petugas, Dosen)</li>
                </ul>

                <h2>Tujuan Pengembangan</h2>
                <p>
                    Dengan sistem ini, proses manajemen lab menjadi lebih terorganisir, data tersimpan dengan aman, dan pemantauan aktivitas lab dapat dilakukan dengan mudah kapan saja.
                </p>

                <h2>Pengembang</h2>
                <p>
                    Sistem ini dikembangkan oleh tim mahasiswa dalam rangka memenuhi kebutuhan sistem informasi laboratorium di lingkungan akademik, serta sebagai bagian dari tugas akhir pemrograman web.
